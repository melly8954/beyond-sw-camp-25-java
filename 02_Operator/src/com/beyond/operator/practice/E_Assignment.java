public class E_Assignment {
    public void method01(){
        int number = 10;

        number += 3;
        System.out.println("number += 3 은(는) " + number);

        number -= 5;
        System.out.println("number -= 5 은(는) " + number);

        number *= 5;
        System.out.println("number *= 6 은(는) " + number);

        number /= 5;
        System.out.println("number /= 5 은(는) " + number);

        number %= 5;
        System.out.println("number %= 5 은(는) " + number);

    }
}
